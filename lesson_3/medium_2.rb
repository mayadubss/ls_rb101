# Question 5
# How can we change this code to make the result easier to predict and easier for the next programmer to maintain?

def tricky_method(a_string_param, an_array_param)
  a_string_param += "rutabaga"
  an_array_param << "rutabaga"
end

my_string = "pumpkins"
my_array = ["pumpkins"]
tricky_method(my_string, my_array)

puts "My string looks like this now: #{my_string}"
puts "My array looks like this now: #{my_array}"

# Answer

def not_so_tricky_method(a_string_param, an_array_param)
  a_string_param += "rutabaga"
  an_array_param += ["rutabaga"]

  return a_string_param, an_array_param
end

my_string = "pumpkins"
my_array = ["pumpkins"]
my_string, my_array = not_so_tricky_method(my_string, my_array)

puts "My string looks like this now: #{my_string}"
puts "My array looks like this now: #{my_array}"

# => Using concatination for both variables, then reassigning the original variables in line 226 makes it clear they will change.
# => This is much better practice than having to rely on the behavior of += vs <<

# Question 6
# How could the following method be simplified without changing its return value?

def color_valid(color)
  if color == "blue" || color == "green"
    true
  else
    false
  end
end

def color_valid_short(color)
  color == "blue" || color == "green" ? true : false # Don't even need ? and the rest, ruby will return the evaluate line.
end

puts color_valid("blue")
puts color_valid_short("blue")
puts color_valid("green")
puts color_valid_short("green")
puts color_valid("red")
puts color_valid_short("red")

